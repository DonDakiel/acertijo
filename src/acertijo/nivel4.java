    private void jPanel1MouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseEntered
        jPanel1.addMouseMotionListener(new MouseAdapter() {
            @Override
            public void mouseMoved(MouseEvent e) {
                Point mouseLocation = e.getPoint();

                // Obtén la posición actual del JPanel.
                Point panelLocation = jPanel1.getLocation();

                // Actualiza la posición del JPanel según la posición del mouse.
                jPanel1.setLocation(panelLocation.x + mouseLocation.x - jPanel1.getWidth() / 2,
                    panelLocation.y + mouseLocation.y - jPanel1.getHeight() / 2);

                // Verifica si el JPanel1 ha tocado el JPanel3, JPanel5, JPanel6 o JPanel8.
                if (jPanel3.getBounds().intersects(jPanel1.getBounds()) ||
                    jPanel5.getBounds().intersects(jPanel1.getBounds()) ||
//                    jPanel6.getBounds().intersects(jPanel1.getBounds()) ||
//                    jPanel7.getBounds().intersects(jPanel1.getBounds()) ||
                    jPanel9.getBounds().intersects(jPanel1.getBounds()) ||
                    jPanel10.getBounds().intersects(jPanel1.getBounds()) ||
                    jPanel11.getBounds().intersects(jPanel1.getBounds()) ||
                    jPanel8.getBounds().intersects(jPanel1.getBounds())) {
                    // Restablece la posición del JPanel1 a la posición original.
                    jPanel1.setLocation(originalPanel1Location);
                }

                // Verifica si el JPanel1 ha tocado el JPanel4.
                if (jPanel4.getBounds().intersects(jPanel1.getBounds())) {
                    // Cierra la ventana actual (this).
                    dispose();
                    nivel3 nivel = new nivel3();
                    nivel.setVisible(true);
                }

                // Vuelve a pintar el JPanel.
                jPanel1.repaint();
            }
        });
    }//GEN-LAST:event_jPanel1MouseEntered

    private void jPanel1MouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseExited
        jPanel1.removeMouseMotionListener(jPanel1.getMouseMotionListeners()[0]);

        if (!isInsidePanel2) {
            jPanel1.setLocation(originalPanel1Location);
        }
        // Vuelve a pintar el JPanel1
        jPanel1.repaint();
    }//GEN-LAST:event_jPanel1MouseExited

    private void jPanel2MouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MouseEntered
        isInsidePanel2 = true;
        if (bandera) {
            originalPanel1Location = jPanel1.getLocation();
            bandera = false;
        }
        System.out.println("entro");
    }//GEN-LAST:event_jPanel2MouseEntered

    private void jPanel2MouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MouseExited
        jPanel1.setLocation(originalPanel1Location);
        // El mouse ha salido de jPanel2
        isInsidePanel2 = false;

        // Vuelve a pintar el JPanel1
        jPanel1.repaint();
        System.out.println("salio");
    }//GEN-LAST:event_jPanel2MouseExited

